
public class Matematika {

    void pertambahan(int a, int b) {
        int x = a + b;
        System.out.println("Pertambahan = "+a+" + "+b+" = " + x);
    }

    void pengurangan(int a, int b) {
        int x = a - b;
        System.out.println("Pengurangan = "+a+" - "+b+" = " + x);
    }   

    void perkalian(int a, int b) {
        int x = a * b;
        System.out.println("Perkalian = " +a+ " x " +b+ " = " + x);
    }

    void pembagian(int a, int b) {
        float x = a / b;
        System.out.println("Pembagian = " +a+ " / " +b+ " = " + x);
    }
}